package main

import (
	"fmt"
	"net/http"
	"io/ioutil"
)

func main() {
	resp, err := http.Get("https://api.bilibili.com/x/web-interface/view?aid=114514")
	if err != nil {
    	fmt.Println("Hello world")
	}
	defer resp.Body.Close()  // 函数结束时关闭Body
	body, err := ioutil.ReadAll(resp.Body)  // 读取Body
	fmt.Println(string(body))
}



